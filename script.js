      function getPoints(Coordinates) {
        var LL, point;
      for(var i = 0; i<100; i++) {
          for(var j =0; j<100;j++) {
            if(coordinates[i][j] == 'F') {
              LL = generateLL(i,j);
              point = new google.maps.LatLng(LL[0], LL[1]);
              fireCoordinates.push(point);
            } else if(coordinates[i][j] == 'P') {
              LL = generateLL(i,j);
              point = new google.maps.LatLng(LL[0], LL[1]);
              pathCoordinates.push(point);
            } else if(coordinates[i][j] == 'V') {
              LL = generateLL(i,j);
              point = new google.maps.LatLng(LL[0], LL[1]);
              victimCoordinates.push(point);
            }
          }
        }
       }

        function generateLL(x, y) {
          var left = -120.981992, horizontal = 0.000034, top = 47.215943, vertical = 0.000024;
          var LL = [];
          var lat, lng;
          lng = left + y*horizontal;
          lat = top - x*vertical;
          LL.push(lat);
          LL.push(lng);
          return LL;
        }

        
     

